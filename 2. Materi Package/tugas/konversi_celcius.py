def celcius_to_kelvin(celcius:float) -> float:
   '''FUNGSI MENGHITUNG CELCIUS KE KELVIN'''
   kelvin = celcius + 273 
   return kelvin

def celcius_to_fahrenheit(celcius:float) -> float:
   '''FUNGSI MENGHITUNG CELCIUS KE FAHRENHEIT'''
   fahrenheit = celcius * 1.8 + 32
   return fahrenheit

def celcius_to_reamur(celcius:float) -> float:
   '''FUNGSI MENGHITUNG CELCIUS KE REAMUR'''
   reamur = celcius * 0.8
   return reamur